primer_numero=float(input("Ingrese el dividendo:"))
segundo_numero=float(input("Ingrese el divisor:"))
#calculo del cocinete de la divicion
cociente  =  primer_numero / segundo_numero
#Calculo del resto de la  divicion
resto  =  primer_numero % segundo_numero
#salida por pantalla
print ( "La división" , primer_numero , "/" , segundo_numero , "=" , cociente , "y su resto es" , resto )


